# 转换为整型
print(int(1.9))
print(int('10'))
# 转换为浮点型
print(float('1.5'))
print(float(5))
# 转换为字符串
print(str(1.5))
# 转换为list
print(list((1,2,3,4,5)))
# 转换为字典
print(dict((['a',1],['b',2],['c',3])))
# 转换为元组
print(tuple([1,2,3,4,5]))
print(tuple({1,2,3,4,5}))
# 转换为集合
print(set([1,2,3,4,5]))
print(set((1,2,3,4,5)))