# 求绝对值参数为数值
print(abs(-1.2))
# 求列表最小值
print(min([3,2.3,8,-1.5]))
# 求列表最大值
print(max([1,5,-1,10]))
# 求列表的和
print(sum([1,2,3,4,5.1]))
# 排序函数
print(sorted([3,1,9,7,4,2]))
# 获取字符长度、元素个数
print(len('qwer'))
print(len('中国加油！'))
print(len([10,'qwe',1.5]))
# 获取商和余数
print(divmod(10,3))
# 获取乘方数
print(pow(2,4))
# 获取指定位数的小数（四舍五入）
print(round(3.145678,2))
# 生成一个a到b的数组,左闭右开,如以下表示从2到20，每次增加2
for i in range(2,20,2):
    print(i)